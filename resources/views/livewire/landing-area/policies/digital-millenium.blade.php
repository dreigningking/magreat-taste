                <li><strong>False Claims.</strong> Submitting a DMCA notice or counter-notice with knowing misrepresentation of infringement or non-infringement may subject you to statutory damages, including costs and attorneys' fees (§512(f)).</li>
                <li><strong>Account Impact.</strong> We may suspend or terminate Users who file dishonest notices or counter-notices.</li>
            </ul>

            <h2>6. Non-U.S. Copyright Owners</h2>
            <p>Although the DMCA is U.S. legislation, Wonegig applies this procedure globally to streamline takedown requests. Rights holders outside the U.S. should still follow the steps in §2. We also honor properly formatted notices under equivalent regimes (e.g., EU Copyright Directive, UK CDPA) and may request additional information to verify standing.</p>

            <h2>7. Repeat-Infringer Policy</h2>
            <ol class="list-decimal pl-6">
                <li>Strike System. Two (2) valid takedown notices = warning; three (3) = automatic account suspension pending appeal.</li>
