            <h2>2. Filing a DMCA Takedown Notice</h2>
            <p>Under §512(c)(3) a valid notice must include all the elements below. Incomplete submissions will be rejected.</p>
            <ol class="list-decimal pl-6">
                <li>Your contact information – full name, mailing address, telephone number, and email.</li>
                <li>Identification of the copyrighted work you claim is infringed (URL to an authorized version or a clear textual description).</li>
                <li>Identification of the infringing material located on Wonegig – one or more direct URLs that point to the specific page(s), file(s), or project chat messages.</li>
                <li>A statement of good-faith belief that use of the material is not authorized by the copyright owner, its agent, or the law (e.g., fair use).</li>
                <li>A statement, under penalty of perjury, that the information in the notice is accurate and that you are the copyright owner or are authorized to act on behalf of the owner.</li>
                <li>Your physical or electronic signature (typing your full legal name on an email counts as an electronic signature).</li>
            </ol>
            <p>Send the completed notice to: <a href="mailto:dmca@wonegig.com" class="text-primary underline">dmca@wonegig.com</a><br>
            Subject line: “DMCA Takedown Notice – [Your Work Title]”</p>

            <h2>3. Wonegig's Response Procedure</h2>
            <ol class="list-decimal pl-6">
                <li>Acknowledgment. We email confirmation of receipt within 48 hours (business days).</li>
                <li>Initial Review. Obvious, facially valid notices trigger temporary disabling or removal of the allegedly infringing content.</li>
                <li>Forwarding. We forward the notice (including your contact details) to the User who posted the content so they understand why it was removed and may respond.</li>
                <li>Repeat-Infringer Policy. Accounts receiving two (2) or more valid DMCA notices within a rolling 12-month period may face suspension or termination, at our discretion.</li>
                <li>Record-keeping. We maintain a log of DMCA notices and actions to preserve safe-harbor eligibility.</li>
            </ol>

            <h2>4. Submitting a Counter-Notification</h2>
            <p>If your content was removed and you believe it is non-infringing or qualifies for fair use, U.S. law permits you to send a counter-notification (§512(g)(3)). It must contain:</p>
            <ol class="list-decimal pl-6">
                <li>Your contact information – full name, mailing address, telephone number, and email.</li>
                <li>Identification of the material that was removed or disabled and the Wonegig URL(s).</li>
                <li>A statement, under penalty of perjury, that you have a good-faith belief the material was removed or disabled due to mistake or misidentification.</li>
                <li>A statement consenting to jurisdiction of the Federal District Court for the district where your address is located (or, if outside the U.S., consenting to jurisdiction of any judicial district where Wonegig may be found) and agreeing to accept service of process from the claimant.</li>
                <li>Your physical or electronic signature (full legal name).</li>
            </ol>
            <p>Send the counter-notice to: <a href="mailto:dmca@wonegig.com" class="text-primary underline">dmca@wonegig.com</a><br>
            Subject line: “DMCA Counter-Notice – [Removed Content Reference]”</p>
            <p><strong>Restoration timeline:</strong> Unless the original claimant files a lawsuit within 10–14 business days after we forward your counter-notice, we may restore the disputed content.</p>

            <h2>5. Misrepresentation &amp; Penalties</h2>
