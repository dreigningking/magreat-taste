                        <tr><td class="border px-2 py-1">1 — Alert</td><td class="border px-2 py-1">We notify the Freelancer and temporarily debit their Wonegig balance (or future earnings) for the chargeback amount + provisional fee.</td></tr>
                        <tr><td class="border px-2 py-1">2 — Evidence</td><td class="border px-2 py-1">Freelancer has 3 business days to submit proof of work (files, timestamps, communications). Wonegig compiles platform data (IP logs, acceptance clicks, escrow confirmations).</td></tr>
                        <tr><td class="border px-2 py-1">3 — Representment</td><td class="border px-2 py-1">Wonegig (via acquiring bank/processor) contests the chargeback.</td></tr>
                        <tr><td class="border px-2 py-1">4 — Outcome</td><td class="border px-2 py-1">a. Win → funds and fee returned to Freelancer; Client may face account suspension.  b. Lose → chargeback stands; Client and Freelancer encouraged to mediate off-platform if additional remedy sought.</td></tr>
                    </tbody>
                </table>
