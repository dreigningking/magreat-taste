            <h2>5 — Disputes &amp; Mediation</h2>
            <ol class="list-decimal pl-6">
                <li>Initiate – Either party selects "Open Dispute" and uploads evidence (deliverables, chat logs, screenshots, or screen-recordings).</li>
                <li>Mediation (24–72 h) – Wonegig Risk Team reviews submissions, may request clarifications, and proposes a settlement.</li>
                <li>Binding Decision – If parties accept, escrow funds distribute accordingly.</li>
                <li>Escalation to Arbitration – If either party rejects the mediation result, the matter follows the Arbitration clause in the Terms &amp; Conditions. Escrow stays frozen until final award.</li>
            </ol>

