            <h2>3 — Funding &amp; Escrow Workflow</h2>
            <ol class="list-decimal pl-6">
                <li>Upfront Funding – Client deposits 100% of the agreed milestone budget into escrow before work commences.</li>
                <li>Verification – High-risk or large transactions may require enhanced KYC or two-factor authentication.</li>
                <li>Work Delivery – Freelancer submits deliverables and clicks "Mark as Delivered."</li>
                <li>Client Review – Within the Dispute Window Client must either:
                    <ul class="list-disc pl-6">
                        <li>Accept Work → escrow auto-releases; or</li>
                        <li>Open Dispute → see § 5.</li>
                    </ul>
                </li>
                <li>Auto-Release – If Client takes no action by 23:59 UTC on the final day of the Dispute Window, funds auto-release to the Freelancer.</li>
            </ol>

