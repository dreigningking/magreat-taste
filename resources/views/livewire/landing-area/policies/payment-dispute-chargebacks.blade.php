            <p><strong>Chargeback Fee:</strong> USD $25 per dispute plus any network-imposed fee. Assessed to the party responsible for the reversal once liability is determined.</p>

            <h2>7 — Prohibited &amp; High-Risk Behaviors</h2>
            <ul class="list-disc pl-6">
                <li>Filing false or abusive chargebacks after accepting work.</li>
                <li>Requesting off-platform payment to avoid fees.</li>
                <li>Delivering plagiarized, non-functional, or AI-generated work without disclosure.</li>
                <li>Collusion between Client and Freelancer to defraud Wonegig (e.g., fake gigs).</li>
            </ul>
            <p><strong>Consequences:</strong> Immediate account suspension, forfeiture of balances, recovery of losses via collections, and potential legal action.</p>

